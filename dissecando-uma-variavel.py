a = input('Digite Algo:')
print('\033[30;40m O Tipo primitivo  desse valor é',type(a))
print('\033[31;40m Só tem espaços?',a.isspace())
print('\033[32;40m É um número?',a.isnumeric())
print('\033[33;40m É alfabetico?',a.isalpha())
print('\033[34;40m É alfanúmerico?',a.isalnum())
print('\033[35;40m Está em Maísculas?',a.isupper())
print('\033[36;40m Está em Minisculas?',a.islower())
print('\033[37;40m Está capitalizada?',a.istitle())